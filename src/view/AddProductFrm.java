        String id = txtProductId.getText();
        String name = txtProductName.getText();
        double price = 0;
        try {
            price = Double.parseDouble(txtProductPrice.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đơn giá ở dạng số !");
        }
        String unit = txtUnit.getText();
        String type = txtProductType.getText();
        int status = 1;
        
        if (id.equals("") && name.equals("") && unit.equals("") && type.equals("") ) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin !");
        } else {
            Product p = new Product(id, name, 1, price,unit, status, type );
            ProductDAO.getInstance().addProduct(p);
            this.dispose();
            owner.loadDataToTable();
            
        }
        
    
