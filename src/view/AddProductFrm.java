    /**
     * @param args the command line arguments
     */
    public String createId() throws SQLException {
        ArrayList<Product> product = ProductDAO.getInstance().getAllProduct();
        ArrayList<Product> productA = new ArrayList<Product>();
        for (Product p : product) {
            if (p.getId().contains("HH")) {
                productA.add(p);
            }
        }
        int i = productA.size();
        String check ="check";
        while(check.length()!=0){
            i++;
            for (Product p : productA) {
                if(p.getId().equals("HH"+i)){
                    check="";
                }
            }
            if(check.length()==0){
                check ="check";
            } else {
                check = "";
            }
        }
        return "HH" + i;
    }


